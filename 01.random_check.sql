FROM dates as dates1
JOIN dates as dates2
ON DATE_PART('day',  dates2.date::timestamp - dates1.date::timestamp) between 2 and 7;

select DISTINCT sub.second_date
from (SELECT dates1.date AS first_date, dates2.date AS second_date
FROM dates as dates1
JOIN dates as dates2
ON DATE_PART('day',  dates2.date::timestamp - dates1.date::timestamp) between 2 and 7) as sub
order by sub.second_date;